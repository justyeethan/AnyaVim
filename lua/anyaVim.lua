function getRandomVariable(hashmap)
  local keys = {}
  for key, _ in pairs(hashmap) do
    table.insert(keys, key)
  end

  local randomIndex = math.random(1, #keys)
  local randomKey = keys[randomIndex]
  return hashmap[randomKey]
end

